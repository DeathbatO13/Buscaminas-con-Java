    private void radioFacilItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_radioFacilItemStateChanged
        if(radioFacil.isSelected()){
            this.spinnerAncho.setValue(Juego.ANCHO_FACIL);
            this.spinnerLargo.setValue(Juego.LARGO_FACIL);
            this.spinnerMinas.setValue(Juego.MINAS_FACIL);
        }
    }//GEN-LAST:event_radioFacilItemStateChanged

